import static java.lang.Math.max;
import static java.lang.Math.min;
import static java.lang.Math.sqrt;
public class StaticImportExample {
    public static void main(String[] args) {
        out.println(max(3,4));
        out.println(min(3,4));
        out.println(sqrt(9));
    }
}
